package com.pay.base.fi.model;

/**
 * 
 * @author PengJiangbo
 *
 */
public class PartnerConfig {

	private Long partnerConfigNo ;
	
	private Long memberCode ;
	
	private String paramCode ;
	
	private String description ;
	
	private String value ;
	
	private String remark ;

	/**
	 * @return the partnerConfigNo
	 */
	public Long getPartnerConfigNo() {
		return partnerConfigNo;
	}

	public void setPartnerConfigNo(Long partnerConfigNo) {
		this.partnerConfigNo = partnerConfigNo;
	}

	/**
	 * @return the memberCode
	 */
	public Long getMemberCode() {
		return memberCode;
	}

	public void setMemberCode(Long memberCode) {
		this.memberCode = memberCode;
	}

	/**
	 * @return the paramCode
	 */
	public String getParamCode() {
		return paramCode;
	}

	public void setParamCode(String paramCode) {
		this.paramCode = paramCode;
	}

	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
	
}
